#include <iostream>
using namespace std;

int main()
{
    int A;
    int B;

    cin  >> A;
    cin >> B; 
    //Suma
    int R = A + B;
    cout << R << endl;
    //Resta
    R = A - B;
    cout  << R << endl;
    //Division
    R = A / B;
    cout << R << endl;
    //Multiplicacion
    R  = A * B;
    cout  << R << endl;

    return 0;
}
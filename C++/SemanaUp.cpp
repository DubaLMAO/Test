#include <bits/stdc++.h>
using namespace std;

int main(){

    int num;
    cin >> num;

    switch (num)
    {
    case 1:
        cout << "lunes";
        break;
    case 2:
        cout << "martes";
        break;
    case 3:
        cout << "miercoles";
        break;
    case 4:
        cout << "jueves";
        break;
    case 5:
        cout << "viernes";
        break;
    case 6:
        cout << "sabado";
        break;
    case 7:
        cout << "domingo";
        break;
    default:
        break;
    }

    return 0;
}
